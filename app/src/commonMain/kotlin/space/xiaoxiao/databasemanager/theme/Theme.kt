/**
 * 数据库管理器主题
 * @param colorTheme 颜色主题（包含是否为暗色模式的信息）
 * @param content 内容
 */
@Composable
fun DatabaseManagerTheme(
    colorTheme: ColorTheme = ColorTheme.GREEN,
    content: @Composable () -> Unit
) {
    val colorScheme = if (colorTheme.isDark) {
        colorTheme.colorScheme.darkScheme
    } else {
        colorTheme.colorScheme.lightScheme
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = AppTypography,
        shapes = AppShapes,
        content = content
    )
}